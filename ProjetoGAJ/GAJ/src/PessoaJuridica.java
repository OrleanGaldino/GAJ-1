
public class PessoaJuridica extends Pessoa{
	private int CNPJ;
	private int Inscrição;
	
	public PessoaJuridica() {
		
	}

	public int getCNPJ() {
		return CNPJ;
	}

	public void setCNPJ(int cNPJ) {
		CNPJ = cNPJ;
	}

	public int getInscrição() {
		return Inscrição;
	}

	public void setInscrição(int inscrição) {
		Inscrição = inscrição;
	}
	
	

}
