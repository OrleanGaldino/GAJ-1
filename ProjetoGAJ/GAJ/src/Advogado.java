
public class Advogado extends Usuario{
	private int numeroOAB;
	private String subSecao;
	private int dataDeInscricao;
	private String situacao;
	private int numeroDeInscrição;
	private String secsinal;
	private String tipoDeInscricao;
	
	public Advogado() {
		
	}
	
	

	public int getNumeroDeInscrição() {
		return numeroDeInscrição;
	}



	public void setNumeroDeInscrição(int numeroDeInscrição) {
		this.numeroDeInscrição = numeroDeInscrição;
	}



	public String getSecsinal() {
		return secsinal;
	}



	public void setSecsinal(String secsinal) {
		this.secsinal = secsinal;
	}



	public String getTipoDeInscricao() {
		return tipoDeInscricao;
	}



	public void setTipoDeInscricao(String tipoDeInscricao) {
		this.tipoDeInscricao = tipoDeInscricao;
	}



	public int getNumeroOAB() {
		return numeroOAB;
	}

	public void setNumeroOAB(int numeroOAB) {
		this.numeroOAB = numeroOAB;
	}

	public String getSubSecao() {
		return subSecao;
	}

	public void setSubSecao(String subSecao) {
		this.subSecao = subSecao;
	}

	public int getDataDeInscricao() {
		return dataDeInscricao;
	}

	public void setDataDeInscricao(int dataDeInscricao) {
		this.dataDeInscricao = dataDeInscricao;
	}

	public String getSituacao() {
		return situacao;
	}

	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}
	
	

}
